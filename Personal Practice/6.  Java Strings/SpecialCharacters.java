
public class SpecialCharacters {
	
	/* 
	 * Special characters : are characters that have special meanings in Java 
	 * and are represented using escape sequences.
	 * Common escape sequences include \n for a new line, \t for a tab, \" for a double quote, etc.
	 */
	
	 public static void main(String[] args) {
		 String message = "Hello,\n\t\"Java\"!";
		 // we use the escape sequence \n for a new line and \t for a tab. 
		 
		 System.out.println(message);
	 }

}
