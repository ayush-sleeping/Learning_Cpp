
public class Loops {

	public static void main(String[] args) {		
//		 Loops examples :
		
		
//		For Loop :	
		for(int i =0; i<10; i++) {
			System.out.println("0" + i);
		}
		
//		While Loop :	
		int a = 23;
		while(a <= 100) {
			System.out.println(a);
			a++;
		}
		
//		Do while :
		int b = 11;
		do {
			System.out.println(b);
			b++;
		}while(b<100);

	}
 
}
